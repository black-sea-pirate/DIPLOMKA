/* ---------------------------------------------------------------------------
 * src/components/Charts/register.js
 *
 * Chart.js ― «ядро» графиков.  Библиотека модульная: прежде чем строить
 * любую диаграмму, нужно *зарегистрировать* те типы элементов и скейлов,
 * которые будут использоваться.  Если этого не сделать, Chart.js выбросит
 * runtime-ошибку «element not registered».
 *
 * Мы вынесли регистрацию в отдельный файл, чтобы:
 *   1) не повторять код в каждом BarChart / RadarChart и т. д.;
 *   2) Tree-shaking Vite/ES-build соберёт только реально используемые модули,
 *      а не весь Chart.js целиком.
 * Достаточно один раз `import './register'` внутри компонента-обёртки —
 * после этого любые диаграммы будут работать.
 * ------------------------------------------------------------------------ */

import {
  // объект ChartJS ― «песочница», куда подключаются модули
  Chart as ChartJS,

  /* --- скейлы (оси) --- */
  CategoryScale, // ось X категорий (строки)
  LinearScale, // ось Y числовая
  RadialLinearScale, // радиальная ось (Radar / PolarArea)

  /* --- типы элементов --- */
  BarElement, // столбики BarChart
  PointElement, // точки Line/Radar
  LineElement, // линия Line/Radar

  /* --- сервисные плагины --- */
  Tooltip, // всплывающая подсказка при hover
  Legend, // легенда (метки наборов данных)
} from "chart.js";

/* === Регистрация модулей ===
 * Порядок не важен, но принято сначала скейлы, потом элементы, затем плагины.
 * Зарегистрированные однажды, они «видны» для всех графиков далее.
 */
ChartJS.register(
  CategoryScale,
  LinearScale,
  BarElement,
  RadialLinearScale,
  PointElement,
  LineElement,
  Tooltip,
  Legend
);

